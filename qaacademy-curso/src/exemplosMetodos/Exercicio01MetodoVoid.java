package exemplosMetodos;
public class Exercicio01MetodoVoid {

    public static void metodoUm() {
        System.out.println("Método Um");
    }

    public static void metodoDois() {
        System.out.println("Método Dois");
    }

    public static void metodoTres() {
        System.out.println("Método Três");
    }

    public static void metodoQuatro() {
        System.out.println("Método Quatro");
    }

    public static void metodoCinco (){
        System.out.println("Método Cinco");
    
    }
    
    public static void main(String [] args){
       metodoUm();
       metodoDois();
       metodoTres();
       metodoQuatro();
       metodoCinco();

    }



}